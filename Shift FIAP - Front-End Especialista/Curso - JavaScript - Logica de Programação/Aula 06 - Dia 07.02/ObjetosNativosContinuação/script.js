//Retorna o valor de PI
//Classe MATCH não precisa de instancia
alert(Math.PI);


//Abslouto remove o sinal matematico que vem na frente 
alert(Math.abs(-5.9));


//Propriedades são para guardar dados daquele objeto
//Metodos são ações que executa

// Max pega o valor maior
// Min pega o minimo
alert(Math.max(5, 9, 9, -80));

//Aredonda o numero para o inteiro mais proximo
alert(Math.round(12.2));

//Aredonda para cima
alert(Math.ceil(12.2));

//Aredonda para baixo
alert(Math.floor(12.2));

//Calcular potencia
//Primeiro numero é a base, o segundo é expoente
alert(Math.pow(3, 2));

//Raiz quadrada 
alert(Math.sqrt(16));

//Aredondar inteiro mais proximo da raiza quadrada
alert(Math.round(Math.sqrt(16)));

//Gera numeros randomicos entre 0 e 01
alert(Math.round());

//Crair um dado de 0 a 6
alert(Math.round() * 6);